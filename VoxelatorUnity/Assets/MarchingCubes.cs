	public static void GenerateChunk(GRIDCELL[,,] voxelsData, bool optimized, out int[] indices, out Vector3[] vertices) 
	{
		int lastIndex = 0;
		List<int> totalTriangles = new List<int>();
		List<Vector3> totalVertices = new List<Vector3>();
		List<Color32> totalColors = new List<Color32>();



		// Taking out triangles from every voxel
		for( int x = 0; x < voxelsData.GetLength(0) - 1; x ++ )
		{
			for( int y = 0; y < voxelsData.GetLength(1) - 1; y ++ )
			{
				for( int z = 0; z < voxelsData.GetLength(2) - 1; z ++ )
				{
					//Determine the index into the edge table which
					//tells us which vertices are inside of the surface
					int CubeIndex = 0;
								
					Vector3[] VertexList = new Vector3[12];

//					Color32[] terrainColor = new Color32[]{
//						Color32(0, 0, 128),
//						Color32(0, 128, 255 ), 
//						Color32(240, 240, 64),
//						Color32(32, 160, 0),
//						Color32(224, 224, 0),
//						Color32(128, 128, 128),
//						Color32(255, 255, 255)
//					};


					for(int i = 0; i < 8; i++) 
					{
						if( voxelsData[ x + vertexOffset[i, 0], 
					              		y + vertexOffset[i, 1],
					              		z + vertexOffset[i, 2]].density < 0.0f) 
						{
							CubeIndex |= 1 << i;
						}
					}
					
					//Cube is entirely in/out of the surface
					if (edgeTable[CubeIndex] == 0)
					{
						continue;
					}
					
					
					//Find the vertices where the surface intersects the cube
					for( int i = 0; i < 12; i++ )
					{
						if((edgeTable[CubeIndex] & (1 << i )) == (1 << i ) )
						{
							float offset = Interpolate(	voxelsData[ x + vertexOffset[edgeConnection[i,0], 0],
						                                			y + vertexOffset[edgeConnection[i,0], 1],
						                                			z + vertexOffset[edgeConnection[i,0], 2]].density, 
					
						                     			voxelsData[ x + vertexOffset[edgeConnection[i,1], 0],
						                     						y + vertexOffset[edgeConnection[i,1], 1],
						                     						z + vertexOffset[edgeConnection[i,1], 2]].density);

							// dont know when vertexOffset thing appeared in here, but it had + and did dragon skin
							VertexList[i].x = voxelsData[x,y,z].position.x + (vertexOffset[edgeConnection[i,0],0] * offset * edgeDirection[i,0]);
							VertexList[i].y = voxelsData[x,y,z].position.y + (vertexOffset[edgeConnection[i,0],1] * offset * edgeDirection[i,1]);
							VertexList[i].z = voxelsData[x,y,z].position.z + (vertexOffset[edgeConnection[i,0],2] * offset * edgeDirection[i,2]);
						}
					}


					for(int i = 0; i < 5; i++)
					{
						if(triTable[CubeIndex,3*i] < 0) 
						{
							break;
						}

				
						for(int j = 0; j < 3; j++)
						{
							int localVertIndex = triTable[CubeIndex,3 * i + j];
							int vertIndex = lastIndex; 
							bool newVert = true;

							#region Mesh Optimization. With size of 20, instead of 9888 vert it makes it 1704 with worse quality
							// Global remapping of vertices to save shared ones

							if ( optimized )
							{
								if( x > 0 )	// taking the left voxel
								{
									GRIDCELL left = voxelsData[x - 1,y,z];
								
									if( localVertIndex == 8 ) 
									{ 
										vertIndex = left.edgeIndices[9]; 
										newVert = false; 
									}
									if( localVertIndex == 7 ) 
									{ 
										vertIndex = left.edgeIndices[5]; 
										newVert = false; 
									}
									if( localVertIndex == 11 ) 
									{ 
										vertIndex = left.edgeIndices[10]; 
										newVert = false; 
									}
									if( localVertIndex == 3 ) 
									{ 
										vertIndex = left.edgeIndices[1]; 
										newVert = false; 
									}
								}
								
								if( y > 0 )	// taking the left voxel
								{
									GRIDCELL bottom = voxelsData[x,y - 1,z];
											
									if( localVertIndex == 0 ) 
									{ 
										vertIndex = bottom.edgeIndices[2]; 
										newVert = false; 
									}
									if( localVertIndex == 9 ) 
									{ 
										vertIndex = bottom.edgeIndices[10]; 
										newVert = false; 
									}
									if( localVertIndex == 4 ) 
									{ 
										vertIndex = bottom.edgeIndices[6]; 
										newVert = false; 
									}
									if( localVertIndex == 8 ) 
									{ 
										vertIndex = bottom.edgeIndices[11]; 
										newVert = false; 
									}
								}
								
								if( z > 0 )	// taking the left voxel
								{
									GRIDCELL back = voxelsData[x,y,z - 1];
											
									if( localVertIndex == 0 ) 
									{ 
										vertIndex = back.edgeIndices[4]; 
										newVert = false; 
									}
									if( localVertIndex == 1 ) 
									{ 
										vertIndex = back.edgeIndices[5];
										newVert = false; 
									}
									if( localVertIndex == 2 ) 
									{ 
										vertIndex = back.edgeIndices[6]; 
										newVert = false; 
									}
									if( localVertIndex == 3 ) 
									{ 
										vertIndex = back.edgeIndices[7]; 
										newVert = false; 
									}
								}
								
								if(  voxelsData[x,y,z].edgeIndices[localVertIndex] < 0 )
                           			voxelsData[x,y,z].edgeIndices[localVertIndex] = vertIndex;
								else
								{
									vertIndex = voxelsData[x,y,z].edgeIndices[localVertIndex];
									newVert = false;
								}
							}
							#endregion

                            totalTriangles.Add(vertIndex);

                            if( newVert ) 
                            {
								// The trong problems is that I don't try to share those vertices
								// that have been created inside of current voxel
								totalVertices.Add(VertexList[localVertIndex]);
								totalColors.Add(AddColors(voxelsData[x,y,z].position.y));

								lastIndex = totalVertices.Count;
                            }
						}
					}	

				}
			}
		}
			
		if( totalVertices.Count > 65000 )
		{
			Debug.LogError("Vertices count cannot be more than 65000. Reduce cluster size.");
			vertices = null;
			indices = null;
			return;
		}

		vertices = totalVertices.ToArray();
		indices = totalTriangles.ToArray();
	}


	private static float Interpolate(float v1, float v2)
	{
		float delta = v2 - v1;
		return (delta == 0.0f) ? 0.5f : (0f - v1)/delta;
	}
	
